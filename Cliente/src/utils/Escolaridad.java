package utils;

public class Escolaridad {
	// {"UNIDAD CURRICULAR", "Créd", "Dur", "Tip", "Año", "Conv", "Calificación"}
	private String unidadCurricular;
	private String credito;
	private String duracion;
	private String tipo;
	private String anio;
	private String conv;
	private String calificacion;
	/**
	 * @return the unidadCurricular
	 */
	public String getUnidadCurricular() {
		return unidadCurricular;
	}
	/**
	 * @param unidadCurricular the unidadCurricular to set
	 */
	public void setUnidadCurricular(String unidadCurricular) {
		this.unidadCurricular = unidadCurricular;
	}
	/**
	 * @return the credito
	 */
	public String getCredito() {
		return credito;
	}
	/**
	 * @param credito the credito to set
	 */
	public void setCredito(String credito) {
		this.credito = credito;
	}
	/**
	 * @return the duracion
	 */
	public String getDuracion() {
		return duracion;
	}
	/**
	 * @param duracion the duracion to set
	 */
	public void setDuracion(String duracion) {
		this.duracion = duracion;
	}
	/**
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}
	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getAnio() {
		return anio;
	}
	public void setAnio(String anio) {
		this.anio = anio;
	}
	public String getConv() {
		return conv;
	}
	public void setConv(String conv) {
		this.conv = conv;
	}
	public String getCalificacion() {
		return calificacion;
	}
	public void setCalificacion(String calificacion) {
		this.calificacion = calificacion;
	}
	
}
